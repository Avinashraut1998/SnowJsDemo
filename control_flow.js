// Switch case 

let month = 3;

switch (month) {
    case 1:
        console.log("Jan");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("Mar");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("Default Case");
        
        break;
}

